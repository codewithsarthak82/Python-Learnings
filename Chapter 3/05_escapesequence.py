
name1 = "Sarthak dhiman is a good boy Ruchika is also a good girl Palak is my good friend"
print(name1)

#\n is a escape function creating a new line .
name2 = "Sarthak dhiman is a good boy\nRuchika is also a good girl\nPalak is my good friend"
print(name2)

#\t is a escape function creating a new tab .
name3 = "Sarthak dhiman is a good boy\tRuchika is also a good girl\tPalak is my good friend"
print(name3)

#\" is a escape function creating any word in parenthesis .
name4 = "Sarthak dhiman is a \"good\" boy\nRuchika is also a\"good\"girl\n Palak is my good \"friend\""
print(name4)

name5 = 'Sarthak dhiman is a good \'boy\''
print(name5)

# \r: Carriage Return - Returns the cursor to the beginning 
# of the line (without advancing to the next line).
name6 = "Sarthak\ris\ra\r\"good\"\rboy"
print(name6)

