# lists is a collection of different datatypes simply !

kids = ["Sarthak","Ruchika","Aaryan", 4 , 56.980 ,"Aayush"]

kids[0] = "Ruchika"
kids[1] = "Sarthak"

print(kids[0],",",kids[1])

#here we can see that lists are mutable "unlike_strings"
print(kids)

# we can also use indexing in lists!
print(kids[1:5:2])
print(kids[0:4])

