'''Using union and intersection in PYTHON'''

s1 = {3,5,8,9}
s2 = {45,67,89,99,54,76,55,9,8}

print(s1.union(s2))          #Adds all elements .
print(s1.intersection(s2))   #common elemets from sets .

'''Difference In PYTHON '''
print(s1.difference(s2))     # (s1-s2) jo s2 mai hain .
print(s2.difference(s1))     # (s2-s1) jo s1 mai hain .

