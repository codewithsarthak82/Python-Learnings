# THE FOLLOWING ARE THE METHODS OF SETS -

s = {1,"Ruchika",4,6,89,"Harry","Palak",90,54,"Sarthak",}


# 1.Add - This adds some other data in the set .
s.add(99)
print(s)

# 2.Remove - This removes some data from the set .
s.remove(99)
print(s) 
''' This gives <ERROR> if element not found '''

# 3.Discard - This also removes some data from the set .
s.discard(99)
print(s)
''' This gives nothing if element not found '''

# 4.Pop - This pops out the element from the set .
print(s.pop())
# '''pops out any random element hence not prefered'''

